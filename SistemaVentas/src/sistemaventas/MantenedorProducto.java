    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      boolean existe= verificarProducto(txtcodigo.getText());
       if (existe==false) {
            
        
        try {Producto p=new Producto();
      p.setCodigo(txtcodigo.getText());
      p.setDescripcion(txtDescripcion.getText());
      p.setPrecio(parseInt(txtPrecio.getText()));
      p.setStockMinimo(parseInt(txtStockminimo.getText()));
      p.setStockActual(parseInt(txtStockactual.getText()));
      productos.add(p);
      JOptionPane.showMessageDialog(null, "Producto guardado exitosamente");
      limpiar();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al guardar el producto");
      limpiar(); 
        }
        }else{
            JOptionPane.showMessageDialog(null, "Error al guardar el producto");
        
     limpiar();}
    
    }//GEN-LAST:event_jButton1ActionPerformed
private boolean verificarProducto(String codigo){
    boolean vp=false;
    for (int i = 0; i < productos.size(); i++) {
        if (productos.get(i).getCodigo().equals(codigo)) {
            vp=true;
       
        }
        
    }return vp;
}
    
    private void agregarDatos() {
    String columnas[]=new String[]{"CODIGO","DESCRIPCION","PRECIO","STOCK MINIMO","STOCK ACTUAL"};
    tablacontenido=new DefaultTableModel(null,columnas);
    ListaProductos LP=new ListaProductos();
    Object datos []= new Object[5];
    for (int i = 0; i < LP.largo(); i++) {
        datos[0]=LP.RegresaObje(i).getCodigo();
        datos[1]=LP.RegresaObje(i).getDescripcion();
        datos[2]=Integer.toString(LP.RegresaObje(i).getPrecio());
        datos[3]=Integer.toString(LP.RegresaObje(i).getStockActual());
        datos[4]=Integer.toString(LP.RegresaObje(i).getStockMinimo());
        tablacontenido.addRow(datos);     
    }
   
    this.jTable1.setModel(tablacontenido);
}
